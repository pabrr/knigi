extension UIColor {

	/// #ffc478
	class var primary: UIColor {
		return UIColor(red: 255 / 255.0, green: 197 / 255.0, blue: 120 / 255.0, alpha: 1)
	}

	/// #ff9f1c
	class var primaryVibrant: UIColor {
		return UIColor(red: 255 / 255.0, green: 159 / 255.0, blue: 28 / 255.0, alpha: 1)
	}

	/// #3a3a3a
	class var textColor: UIColor {
		return UIColor(red: 58 / 255.0, green: 58 / 255.0, blue: 58 / 255.0, alpha: 1)
	}

	/// #fAf7f0
	class var secondary: UIColor {
		return UIColor(red: 250 / 255.0, green: 247 / 255.0, blue: 240 / 255.0, alpha: 1)
	}

}
